def convertH_dict(H):
    H_dict = {}
    for i in range(H.shape[0]):
        H_dict[i] = set(np.where(H[i,:] == 1)[0])
    return H_dict

def encode(matrix, encoding):
    k, n = encoding.shape
    splits = np.array_split(matrix, k)
    encodeds = []
    encode_time = 0
    for idx in range(n):
      start_time = time.time()
      if idx < k:
          encoded = splits[idx]
      else:
          code = encoding[:, idx]
          encoded = np.zeros_like(splits[0])
          for split_idx, coeff in enumerate(code):
              encoded += coeff * splits[split_idx]
      encode_time += time.time() - start_time
      #ptFile = '/home/zhifeng/apps/static/data/partition%d.mat' % (idx+1)
      #np.savetxt(ptFile, encoded, fmt='%i')
